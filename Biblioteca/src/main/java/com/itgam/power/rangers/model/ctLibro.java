package com.itgam.power.rangers.model;

public class ctLibro {

	private Integer iLibro;
	private String cNombre;
	private Integer iEditorial;
	private String cAutor;
	private Integer iMateria;
	private Boolean lEstatus;
	private String cObs;
	
	public Integer getiLibro() {
		return iLibro;
	}
	public void setiLibro(Integer iLibro) {
		this.iLibro = iLibro;
	}
	public String getcNombre() {
		return cNombre;
	}
	public void setcNombre(String cNombre) {
		this.cNombre = cNombre;
	}
	public Integer getiEditorial() {
		return iEditorial;
	}
	public void setiEditorial(Integer iEditorial) {
		this.iEditorial = iEditorial;
	}
	public String getcAutor() {
		return cAutor;
	}
	public void setcAutor(String cAutor) {
		this.cAutor = cAutor;
	}
	public Integer getiMateria() {
		return iMateria;
	}
	public void setiMateria(Integer iMateria) {
		this.iMateria = iMateria;
	}
	public Boolean getlEstatus() {
		return lEstatus;
	}
	public void setlEstatus(Boolean lEstatus) {
		this.lEstatus = lEstatus;
	}
	public String getcObs() {
		return cObs;
	}
	public void setcObs(String cObs) {
		this.cObs = cObs;
	}
	
}
